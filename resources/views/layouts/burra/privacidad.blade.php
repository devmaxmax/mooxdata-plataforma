POLÍTICA DE PRIVACIDAD
Última actualización: 1/2/2026

1. Información que Recopilamos
Nuestra aplicación recopila la siguiente información:

a) Información proporcionada por el usuario:
Nombre y perfil público de Facebook
Mensajes y conversaciones con nuestro bot
Pedidos realizados a través del chat
Información de contacto (si se proporciona)

b) Información automática:
ID de usuario de Facebook
Timestamps de interacciones
Tipo de dispositivo y navegador


2. Cómo Usamos la Información
Utilizamos los datos recopilados para:
Procesar y gestionar pedidos de alimentos
Proporcionar respuestas automatizadas mediante nuestro bot de IA
Mejorar la experiencia del usuario y la calidad del servicio
Enviar confirmaciones y actualizaciones de pedidos
Analizar patrones de uso para optimizar el servicio

3. Compartir Información
a) Con MooxData:
La información se procesa a través de MooxData, nuestro proveedor de servicios de inteligencia artificial, para:
Generar respuestas automatizadas
Procesar y gestionar pedidos
Mantener registros de transacciones

b) Con terceros:
No vendemos ni alquilamos tu información personal. Solo la compartimos cuando:
Es necesario para procesar tu pedido (ej. con el establecimiento de comida)
Lo requiere la ley
Con tu consentimiento explícito

4. Almacenamiento y Seguridad
Los datos se almacenan en servidores seguros
Implementamos medidas de seguridad técnicas y organizativas
El acceso está restringido al personal autorizado
Los datos se conservan mientras sea necesario para el servicio

5. Tus Derechos
Puedes:
Solicitar acceso a tus datos personales
Solicitar la corrección de datos incorrectos
Solicitar la eliminación de tus datos
Oponerte al procesamiento de tus datos
Retirar tu consentimiento en cualquier momento
Para ejercer estos derechos, contáctanos a través de nuestra página de Facebook.

6. Política de Meta (Facebook)
Esta aplicación opera bajo los Términos de Servicio y Política de Datos de Meta. Al usar nuestra aplicación, aceptas que compartamos cierta información con Meta según lo requieran sus políticas.

7. Cambios en esta Política
Podemos actualizar esta política ocasionalmente. Publicaremos los cambios en esta página y actualizaremos la fecha de "Última actualización".

